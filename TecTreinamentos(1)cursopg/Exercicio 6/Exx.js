// WHILE ==> ESTRUTURA DE REPETIÇÃO COM TESTE LOGICO NO INICIO
// DO   WHILE ==> ESTRUTURA DE REPETIÇÃO COM TESTE LOGICO NO FINAL.
var contador = 1
while (contador <= 6) {
    console.log(`Contei ${contador}`)
    contador++//teste logico no inicio
}
var contador = 1
do {
    console.log(`Contei ${contador}`)
    contador++//teste logico no final
}while (contador <= 6)

